// 5.Implement addition, subtraction, multiplication, and division using functions

function addition(n, m) {
    return n + m
}
let subtraction = (n, m) => m - n

let multification = function (m, n) {
    return m * n
}
function division(n, m) {
    return m / n
}
a=4 //+prompt("enter a number")
b=8 //+prompt("enter a number")

